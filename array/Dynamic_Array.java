package array;

import java.util.ArrayList;

public class Dynamic_Array {
    public static void main(String[] args) {
        ArrayList<Integer> marks=new ArrayList<>();
        marks.add(20);
        marks.add(55);
        marks.add(35);
        System.out.println(marks);
        marks.remove(1);
        System.out.println(marks);
    }
}
